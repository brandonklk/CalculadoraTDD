package Calculator;

public class Calculator
{
    public Integer sumNumber(Integer valueOne, Integer valueTwo)
    {
        return valueOne + valueTwo;
    }

    public Integer subtractionNumber(Integer valueOne, Integer valueTwo)
    {
        return valueOne - valueTwo;
    }

    public Integer divisionNumber(Integer valueOne, Integer valueTwo)
    {
        return valueOne / valueTwo;
    }

    public Integer multiplicationNumber(Integer valueOne, Integer valueTwo)
    {
        return valueOne * valueTwo;
    }

}
